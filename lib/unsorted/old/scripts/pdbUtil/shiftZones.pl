# getCommandLineOptions()
#
#  rets: \%opts  pointer to hash of kv pairs of command line options
#
sub getCommandLineOptions {
    use Getopt::Long;
    my $usage = qq{usage: $0 -file <file> -shift <shift_amt> -side <query/parent>\n};

    # Get args
    my %opts = ();
    &GetOptions (\%opts, "file=s", "shift=i", "side=s");

    # Check for legal invocation
    if (! defined $opts{file}  ||
	! defined $opts{shift} 
        ) {
        print STDERR "$usage\n";
        exit -1;
    }
    &checkExist ('f', $opts{file});

    # defaults
    $opts{side} = 'query' if (! defined $opts{side});

    return %opts;
}

